/** 30-day series — slightly noisy, gently trending up. */
export const mockTraffic = (() => {
  const out = []
  let v = 1100
  for (let i = 29; i >= 0; i--) {
    v += Math.round((Math.random() - 0.4) * 220)
    v = Math.max(400, v)
    out.push({
      date: new Date(now - i * 24 * 60 * 60 * 1000).toISOString().slice(5, 10),
      views: v,
      visitors: Math.round(v * (0.55 + Math.random() * 0.15)),
    })
  }
  return out
})()

